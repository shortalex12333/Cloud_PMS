# Entity Extraction Testing Framework
# ====================================
#
# This package provides comprehensive testing for maritime entity extraction.
#
# Modules:
# - document_miner: Extracts terms from yacht documents (reverse engineering)
# - ground_truth: Golden dataset creation and management
# - extraction_validator: Precision/Recall/F1 testing
# - gap_analyzer: Identifies missing patterns and false positives
# - metrics_dashboard: Success metrics and reporting
